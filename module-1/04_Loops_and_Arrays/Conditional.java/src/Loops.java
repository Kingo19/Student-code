public class Loops {
    public static void main(String[] args) {
//        While Loop
        int x = 0;
        while (x<11) {
            System.out.println(x);
            x++;

        }
//        Do while Loop.
        int i = 4;
        do {
            System.out.println(i);
            i++;
        } while (i<5);

//     For Loop.
        for (int u = 5; u <=20; u++) {
            System.out.println(u);
        }
    }
}
