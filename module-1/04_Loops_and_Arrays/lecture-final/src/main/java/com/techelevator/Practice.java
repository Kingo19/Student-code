package com.techelevator;

public class Practice {

    public static void main(String[] args) {

        //increment (prefix and postfix)
        int i = 10;

        System.out.println(i);

        //prefix (++i)
        System.out.println(++i);

        //postfix (i++)
        i = 10;

        System.out.println(i);
        System.out.println(i++);
        System.out.println("after:" + i);


    }
}
